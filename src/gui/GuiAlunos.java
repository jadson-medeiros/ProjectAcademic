    private void preencherTabela() 
    {
        try 
        {
            // TODO add your handling code here:
            List<Aluno> alunos = new DadosAluno().listar();
            
            DefaultTableModel table = (DefaultTableModel) TblAluno.getModel();
            
            int linhas = table.getRowCount();
            
            for (int i = 0;i < linhas; i++) 
            {
                table.removeRow(0);
            }
            
            for (Aluno a: alunos) 
            {
                Object linha[] = {a, a.getNome(),a.getEndereco(), a.getTelefones(), a.getDataIngresso()};
                table.addRow(linha);
            }            
        } 
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }
    }
    
