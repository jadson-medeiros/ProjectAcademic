    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:                
        cboCurso.removeAllItems();
        cboDisciplina.removeAllItems();
        cboProfessor.removeAllItems();
        List<Curso> cursos;
        List<Disciplina> disciplinas;
        List<Professor> professores;
        try 
        {
            cursos = new DadosCurso().listar();
            for (Curso curso: cursos)
            {
                cboCurso.addItem(curso);
            }
            
            disciplinas = new DadosDisciplina().listar();
            for (Disciplina disc: disciplinas)
            {
                cboDisciplina.addItem(disc);
            }
            
            professores = new DadosProfessor().listar();
            for (Professor prof: professores)
            {
                cboProfessor.addItem(prof);
            }
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(GuiTurma.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_formWindowActivated

