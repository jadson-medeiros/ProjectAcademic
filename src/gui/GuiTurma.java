    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Turma turma = new Turma(((Curso)cboCurso.getSelectedItem()),((Disciplina)cboDisciplina.getSelectedItem()), ((Professor)cboProfessor.getSelectedItem()));
        
        Dado d = new DadosTurma();
        try 
        {
            d.incluir(turma);
            this.dispose();
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(GuiTurmas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

