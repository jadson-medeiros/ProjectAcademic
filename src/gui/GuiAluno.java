    private void BtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGravarActionPerformed
        // TODO add your handling code here:
        Aluno a = new Aluno (TxtNome.getText(),TxtEndereco.getText(), (Usuario) cboUsuarios.getModel().getSelectedItem());
        a.addTelefone(new Telefone(TxtDDD.getText(), TxtTelefone.getText()));
        try
        {
            Dado d = new DadosAluno();
            d.incluir(a);
            JOptionPane.showMessageDialog(null,"Aluno cadastrado com sucesso!");
            this.dispose();
        }
        catch (Exception ex)
        {
            Logger.getLogger(GuiAlunos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnGravarActionPerformed

