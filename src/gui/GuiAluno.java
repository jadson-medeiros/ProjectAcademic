    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:                
        cboUsuarios.removeAllItems();
        
        try 
        {
            List<Usuario> usuarios = new DadosUsuario().listar();
            for (Usuario usuario: usuarios)
            {
                if(usuario.getTipo().equals(TipoUsuario.Aluno))
                {
                    cboUsuarios.addItem(usuario);
                }
            }            
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(GuiTurmas.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_formWindowActivated

